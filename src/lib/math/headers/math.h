#ifndef MATH_H
#define MATH_H

// Get the maximum between two int pointer
int* max2ip(int* a, int* b);

// Get the maximum between two float pointer
float* max2fp(float* a, float* b);

// Get the maximum between two int
int max2i(int a, int b);

// Get the maximum between three int
int max3i(int a, int b, int c);

// Get the minimum between two int
int min2i(int a, int b);

// Get the minimum between three int
int min3i(int a, int b, int c);

// Get the max between two float
float max2f(float a, float b);

// Get the minimum between two float
float min2f(float a, float b);

#endif